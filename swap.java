

class swap {
 
    // Function to swap two numbers
    // Using temporary variable
    public static void main(String[] args)
    {int m =4; int n =2;
        // Swapping the values
        int temp = m;
        m = n;
        n = temp;
        System.out.println("Value of m is " + m
                           + " and Value of n is " + n);
    }
}